import streamlit as st
import torch, joblib
from transformers import DistilBertTokenizerFast, DistilBertForSequenceClassification

@st.cache_resource
def load_model():
    MODEL_PATH = "./final_model"
    tokenizer = DistilBertTokenizerFast.from_pretrained(MODEL_PATH)
    model     = DistilBertForSequenceClassification.from_pretrained(MODEL_PATH)
    encoder   = joblib.load(f"{MODEL_PATH}/label_encoder.pkl")
    model.eval()
    return tokenizer, model, encoder

tokenizer, model, encoder = load_model()

def predict_disease(text: str, top_k: int = 5) -> list:
    """
    Predict disease from a free-text symptom description.
    Returns top_k predictions sorted by confidence (highest first).
    """
    model.eval()
    device = next(model.parameters()).device

    inputs = tokenizer(
        text,
        return_tensors="pt",
        truncation=True,
        max_length=256,
        padding=True,
    )
    inputs = {k: v.to(device) for k, v in inputs.items()}

    with torch.no_grad():
        logits = model(**inputs).logits

    probs       = torch.softmax(logits, dim=-1).squeeze().cpu().numpy()
    top_indices = probs.argsort()[::-1][:top_k]

    return [
        {"disease": encoder.classes_[i], "confidence": round(float(probs[i]) * 100, 2)}
        for i in top_indices
    ]

