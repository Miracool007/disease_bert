user_input = st.text_area("Describe how you feel:", height=120)

if st.button("Predict") and user_input:
    results = predict_disease(user_input, top_k=5)  # same function as above
    st.subheader("Top predictions")
    for r in results:
        st.write(f"**{r['disease'].title()}** — {r['confidence']}% confidence")